package com.example.DigitalWayfinder.dto;

public class DataCloudRequest {

private String cloudUsage;
    private String realTimeIntegration;
    private String unifiedDataModel;
    private String externalIntegration;

    // Getters and setters
    public String getCloudUsage() {
        return cloudUsage;
    }

    public void setCloudUsage(String cloudUsage) {
        this.cloudUsage = cloudUsage;
    }

    public String getRealTimeIntegration() {
        return realTimeIntegration;
    }

    public void setRealTimeIntegration(String realTimeIntegration) {
        this.realTimeIntegration = realTimeIntegration;
    }

    public String getUnifiedDataModel() {
        return unifiedDataModel;
    }

    public void setUnifiedDataModel(String unifiedDataModel) {
        this.unifiedDataModel = unifiedDataModel;
    }

    public String getExternalIntegration() {
        return externalIntegration;
    }

    public void setExternalIntegration(String externalIntegration) {
        this.externalIntegration = externalIntegration;
    }

}
